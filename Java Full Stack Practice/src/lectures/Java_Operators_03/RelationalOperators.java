package lectures.Java_Operators_03;

public class RelationalOperators {
	public static void main(String[] args) {
		int a =30,b = 10;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
	}

}
